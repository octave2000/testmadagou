export const DEFAULT_LANGUAGE: Language = "fr";

let currentLanguage: Language = DEFAULT_LANGUAGE;
const listeners = new Set<() => void>();

export const getLanguage = (): Language => currentLanguage;

export const setLanguage = (language: Language) => {
  if (language === currentLanguage) return;
  currentLanguage = language;
  if (typeof window !== "undefined") {
    window.localStorage.setItem(STORAGE_KEY, language);
    document.documentElement.lang = language;
  }
  listeners.forEach((listener) => listener());
};

export const hydrateLanguage = () => {
  if (typeof window === "undefined") return;
  const stored = window.localStorage.getItem(STORAGE_KEY);
  if (stored === "en" || stored === "fr") {
    setLanguage(stored);
  }
};

export const subscribe = (listener: () => void) => {
  listeners.add(listener);
  return () => listeners.delete(listener);
};

const resolvePath = (obj: Translations, path: string): string | undefined => {
  return path
    .split(".")
    .reduce<Translations | string | undefined>((acc, key) => {
      if (acc && typeof acc === "object" && key in acc) {
        return acc[key] as Translations | string;
      }
      return undefined;
    }, obj) as string | undefined;
};

const interpolate = (value: string, vars?: Record<string, string | number>) => {
  if (!vars) return value;
  return Object.entries(vars).reduce(
    (acc, [key, replacement]) =>
      acc.replaceAll(`{{${key}}}`, String(replacement)),
    value,
  );
};

export const translate = (
  language: Language,
  key: string,
  vars?: Record<string, string | number>,
) => {
  const value =
    resolvePath(translations[language], key) ||
    resolvePath(translations.en, key);
  if (!value) return key;
  return interpolate(value, vars);
};

export const t = (key: string, vars?: Record<string, string | number>) =>
  translate(getLanguage(), key, vars);

const humanizeEnum = (value: string) => value.replace(/([A-Z])/g, " $1").trim();

export const translateEnum = (
  group: "amenities" | "highlights" | "propertyTypes" | "listingLabels",
  value: string,
  language: Language = getLanguage(),
) => {
  const key = `enums.${group}.${value}`;
  const translated = translate(language, key);
  return translated === key ? humanizeEnum(value) : translated;
};
